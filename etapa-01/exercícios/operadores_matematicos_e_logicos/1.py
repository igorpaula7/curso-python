# Enunciado: Peça dois números e exiba o resultado das quatro operações básicas entre eles.

a = int(input("Informe o primeiro número: "))
b = int(input("Informe o segundo número: "))

print("soma: ", (a + b))
print("subtração: ", (a - b))
print("multiplicação: ", (a * b))
print("divisão: ", (a / b))